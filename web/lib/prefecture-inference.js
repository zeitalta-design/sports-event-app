const ONLINE_PATTERNS = /ONLINE|オンライン|バーチャル|TATTA\s*RUN|セルフチャレンジ|リモート|どこでも|Virtual|テレワーク/i;

/**
 * イベントがオンライン/バーチャル大会かどうかを判定
 */
export function isOnlineEvent(title, description) {
  const text = (title || "") + " " + (description || "");
  return ONLINE_PATTERNS.test(text);
}

/**
 * テキストから都道府県を推定する
 * @param {string[]} texts - 検索対象のテキスト配列（優先度順）
 * @returns {{ prefecture: string|null, source: string, confidence: string }}
 */
export function inferPrefecture(...texts) {
  const combined = texts.filter(Boolean).join(" ");

  if (!combined.trim()) {
    return { prefecture: null, source: "no_text", confidence: "none" };
  }

  // Step 1: 明示的な都道府県名を検索（最高信頼度）
  for (const pref of PREFECTURES) {
    if (combined.includes(pref)) {
      return { prefecture: pref, source: "explicit", confidence: "high" };
    }
  }

  // Step 2: 市区町村名からマッピング（中信頼度）
  for (const [city, pref] of Object.entries(CITY_TO_PREFECTURE)) {
    // 「○○市」「○○区」形式、またはそのまま含む
    if (combined.includes(city + "市") || combined.includes(city + "区") ||
        combined.includes(city + "町") || combined.includes(city + "村")) {
      return { prefecture: pref, source: `city:${city}`, confidence: "high" };
    }
    // 単独キーワード（やや緩い）
    if (combined.includes(city)) {
      return { prefecture: pref, source: `keyword:${city}`, confidence: "medium" };
    }
  }

  return { prefecture: null, source: "not_found", confidence: "none" };
}
